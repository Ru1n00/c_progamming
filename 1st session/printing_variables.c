#include <stdio.h>  // std - standard  io - input output

int main() {

    int x = 50;
    int y = 20;

    int sum = x + y;


    // identifiers
    // %d = integer
    // %f = float
    // %lf = double
    // %c = char
    // The sum of 50 and 20 is 70
    printf("The sum of %d and %d is %d", x, y, sum);

    return 0;
}